// 返回y年m月d日是星期几
int Zeller(int y, int m, int d) {
  if (m == 1 || m == 2) {
    --y;
    m += 12;
  }
  int c = y / 100;
  y %= 100;
  //1582年10月4日之前
  return ((y + y / 4 + c / 4 - 2 * c + 13 * (m + 1) / 5 + d + 2) % 7) + 7 % 7;
  //1582年10月4日之后
  return ((y + y / 4 + c / 4 - 2 * c + 26 * (m + 1) / 10 + d - 1) % 7 + 7) % 7;
}